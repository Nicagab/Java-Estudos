public class P0205EstruturasDeRepeticao {
    public static void main(String[] args) {

        // Diretiva for
        for(int i = 0; i<=10;i++){
            System.out.println(i);
        }

        int i = 0;

        // Diretiva While
        while(i<=10){
            System.out.println(i);
            i++;
        }

        i = 0;
        // Diretiva Do While
        do {
            System.out.println(i);
            i++;
        } while (i<=10);
    }
}
